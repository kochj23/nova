class TestRetry(unittest.TestCase):

    def test_embed_raises_on_network_failure(self):
        """embed() raises on network failure (caller handles retry)."""
        with patch("urllib.request.urlopen", side_effect=OSError("refused")):
            with self.assertRaises(Exception):
                embed("test text", "model")

    def test_main_counts_errors_without_crashing(self):
        """main() must count errors and continue when embedding fails."""
        mock_conn = MagicMock()
        mock_cur = MagicMock()
        mock_conn.cursor.return_value = mock_cur
        mock_cur.fetchone.side_effect = [
            (100,),        # SELECT COUNT(*)
            ([0.1] * 768,), # SELECT embedding
        ]
        mock_cur.fetchmany.side_effect = [
            [(1, "text 1"), (2, "text 2")],
            [],
        ]

        with patch("psycopg2.connect", return_value=mock_conn):
            with patch.object(_mod, "embed", side_effect=Exception("embed failed")):
                with patch.object(_mod, "post_slack"):
                    with patch("sys.argv", ["nova_reembed.py", "--dry-run"]):
                        try:
                            main()
                        except (SystemExit, Exception):
                            pass


