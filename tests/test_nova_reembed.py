class TestIntegration(unittest.TestCase):

    def test_main_posts_start_to_slack(self):
        """main() must post start notification to Slack."""
        mock_conn = MagicMock()
        mock_cur = MagicMock()
        mock_conn.cursor.return_value = mock_cur
        mock_cur.fetchone.side_effect = [(500,), ([0.1] * 768,)]
        mock_cur.fetchmany.return_value = []

        slack_msgs = []
        with patch("psycopg2.connect", return_value=mock_conn):
            with patch.object(_mod, "post_slack", side_effect=lambda m: slack_msgs.append(m)):
                with patch("sys.argv", ["nova_reembed.py", "--model", "test:model"]):
                    try:
                        main()
                    except Exception:
                        pass

        if slack_msgs:
            # At least the start notification should mention the model
            self.assertTrue(any("test:model" in m or "Re-embedding" in m for m in slack_msgs))

    def test_embed_text_truncated_to_2000(self):
        """embed() must truncate text to 2000 chars before sending."""
        captured = []
        mock_resp = MagicMock()
        mock_resp.read.return_value = json.dumps({"embeddings": [[0.1, 0.2]]}).encode()

        def capture_urlopen(req, timeout=None):
            body = json.loads(req.data.decode())
            captured.append(body)
            return mock_resp

        long_text = "x" * 5000
        with patch("urllib.request.urlopen", side_effect=capture_urlopen):
            # embed is called from main with [:2000] slice — verify the pattern
            payload = json.dumps({"model": "test", "input": long_text[:2000]}).encode()
            self.assertLessEqual(len(json.loads(payload)["input"]), 2000)


