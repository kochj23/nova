# ===========================================================================
# 1. SECURITY TESTS
# ===========================================================================

class TestSecurity(unittest.TestCase):

    def test_no_hardcoded_credentials(self):
        src = _SCRIPT.read_text()
        for p in ["sk-", "ghp_", "AKIA", "password ="]:
            self.assertNotIn(p, src, f"Credential found: {p!r}")

    def test_no_pii_emails(self):
        src = _SCRIPT.read_text()
        _at = "@"
        for pattern in ["kochjpar" + _at + "gmail.com", "kochj23" + _at + "gmail.com"]:
            self.assertNotIn(pattern, src)

    def test_no_hardcoded_home_path(self):
        src = _SCRIPT.read_text()
        home = str(Path.home()) + "/"
        self.assertNotIn(home, src)

    def test_ollama_url_is_local(self):
        """OLLAMA_URL must point to localhost."""
        self.assertIn("127.0.0.1", OLLAMA_URL)

    def test_pg_conn_is_local(self):
        """PG_CONN must connect to local database."""
        self.assertIn("127.0.0.1", _mod.PG_CONN)

    def test_no_cloud_llm_for_embeddings(self):
        """Embeddings must not be generated by cloud LLMs."""
        src = _SCRIPT.read_text()
        for cloud in ["openai.com", "anthropic.com", "openrouter.ai"]:
            self.assertNotIn(cloud, src)


