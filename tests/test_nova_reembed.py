# 2. PERFORMANCE TESTS
# ===========================================================================

class TestPerformance(unittest.TestCase):

    def test_batch_size_is_reasonable(self):
        """BATCH_SIZE must be between 10 and 500 for memory efficiency."""
        self.assertGreaterEqual(BATCH_SIZE, 10)
        self.assertLessEqual(BATCH_SIZE, 500)

    def test_embed_fast_on_short_text(self):
        """embed() must complete in < 100ms when Ollama responds immediately."""
        mock_resp = MagicMock()
        mock_resp.read.return_value = json.dumps({
            "embeddings": [[0.1, 0.2, 0.3] * 100]
        }).encode()

        with patch("urllib.request.urlopen", return_value=mock_resp):
            start = time.perf_counter()
            result = embed("short text", "test-model")
            elapsed = time.perf_counter() - start
        self.assertLess(elapsed, 0.1)


